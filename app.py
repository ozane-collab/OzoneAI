import os
import requests
from flask import Flask, request, jsonify
from flask_cors import CORS
from groq import Groq
from openai import OpenAI

app = Flask(__name__)
# 🌐 በፍሮንትኤንድ እና በባክኤንድ መካከል ያለውን የኮኔክሽን ችግር ለመፍታት
CORS(app, resources={r"/*": {"origins": "*"}})

# 🔐 API Keys ከ Render Environment Variables
GROQ_API_KEY = os.environ.get("GROQ_API_KEY", "")
OPENROUTER_API_KEY = os.environ.get("OPENROUTER_API_KEY", "")

# AI Clients ማዋቀር
groq_client = Groq(api_key=GROQ_API_KEY) if GROQ_API_KEY else None
openrouter_client = OpenAI(base_url="https://openrouter.ai/api/v1", api_key=OPENROUTER_API_KEY) if OPENROUTER_API_KEY else None

@app.route("/", methods=["GET", "HEAD"])
def home():
    return "OzoneAI Pro Stable Core is Live!", 200

# 💬 1. CHAT AI ENDPOINT (አማርኛ እና እንግሊዝኛን ሚዛናዊ የሚያደርገው)
@app.route("/api/chat", methods=["POST"])
def chat():
    try:
        data = request.get_json()
        if not data:
            return jsonify({"reply": "⚠️ No data received!"}), 400
            
        user_message = data.get("message", "")
        
        if not groq_client:
            return jsonify({"reply": "⚠️ GROQ_API_KEY is missing on Render!"}), 500

        system_instruction = (
            "You are OzoneAI, a smart AI assistant built by Ozyan Ekubay. "
            "STRICT LANGUAGE DETECTOR:\n"
            "1. If the user writes in English (e.g., 'Hi', 'Hello', 'How are you'), you MUST reply only in English.\n"
            "2. If the user writes in Amharic script (e.g., 'እንዴት ነህ', 'ሰላም'), you MUST reply in Amharic using Ethiopic script.\n"
            "3. If the user writes in Latin-Amharic / Pinglish (e.g., 'Endet neh', 'Selam', 'Srah mndnew'), detect that this is Amharic, and reply in Amharic using Ethiopic script.\n"
            "Be concise, direct, and reply only to what is requested without extra text."
        )

        completion = groq_client.chat.completions.create(
            model="llama-3.1-8b-instant", 
            messages=[
                {"role": "system", "content": system_instruction},
                {"role": "user", "content": user_message}
            ],
            temperature=0.5,
            max_tokens=1024
        )
        return jsonify({"reply": completion.choices[0].message.content})
        
    except Exception as e:
        return jsonify({"reply": f"⚠️ Server Error: {str(e)}"}), 500

# 📸 2. PHOTO AI ENDPOINT (የአንተን ድሮ የ JSON ፎርማት ተከትሎ በንጹህ እንግሊዝኛ የተሰራ)
@app.route("/api/photo", methods=["POST"])
def photo():
    try:
        data = request.get_json()
        if not data:
            return jsonify({"reply": "⚠️ No data received!"}), 400
            
        user_message = data.get("message", "")
        
        # ጽሑፉን ለሊንክ በሚስማማ መልኩ መቀየር (URL Encode)
        encoded_prompt = requests.utils.quote(user_message)
        image_url = f"https://image.pollinations.ai/p/{encoded_prompt}?width=1024&height=1024&nologo=true"
        
        # 🟢 የአንተ ፍሮንትኤንድ በትክክል የሚያነበው ንጹህ እንግሊዝኛ ምላሽ መዋቅር
        return jsonify({
            "reply": "Your image has been successfully generated by Ozone Photo AI:",
            "image": image_url
        })
    except:
        return jsonify({"reply": "⚠️ Image generation failed. Try again with simpler text."})

# 🎬 3. VIDEO AUTOMATION ENDPOINT
